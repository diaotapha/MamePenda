module Riz_au_poisson
    def self.desc
      details = "RIZ AU POISSON
  
  Du poisson
  Des légumes :
  Carottes, manioc, patate douce, navet, chou, aubergine, aubergine amer(diakhatou), piment 
  Oignons, poivron, céleri, tomate, persil, ail
  1kg de riz 
  250ml d’huile 
  Poisson séché (guèdj) et yett 
  Sel, poivre, piment
  Facultatif : crevettes, gombo, Maggi
  
  INSTRUCTIONS
  Farcir les poissons avec le mélange de persil, ail, sel et piment.
  Mettre l’huile dans la marmite. Lorsque c’est chaud rajouter la moitié d’un oignon, du poivron et de l’ail coupés en dés. Rajouter le guèdj et le yett.
  Laisser brunir et rajouter la tomate écrasée et bien mélanger pendant environ 3mn. Ajouter le poisson et puis environ 1l et demi d’eau puis porter à ébullition. Ajouter les légumes sauf le chou et la patate douce pour le moment. 
  Réduire le feu et préparer le nokoss en attendant.
  Pour le nokoss, il faut mixer du piment, du poivre, de l’ail, un oignon du céleri et du Maggi et réserver.
  Faire cuire le riz à la vapeur puis ajouter le nokoss, le reste des légumes et le piment.
  Rectifier l’assaisonnement et Laisser cuire à petit feu pendant 30mn enlever les poissons et légumes puis mettre le riz.
  Refermer la marmite et laisser cuire le riz à point.
  
  PS : en fonction du type de poisson on peut soit les frire au début soit réserver.
  On peut aussi rajouter du gombo et des crevettes.
  Pour le riz au poisson rouge rajouter environ 3 cuillerées à soupe pleines de tomate concentrée." 
      details.to_s
    end
  end
  
  
  module Riz_a_la_viande
