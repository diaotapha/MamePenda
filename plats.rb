  250ml d’huile 
  Oignons, poivron, céleri, ail, piment
  Sel, poivre, piment
  Facultatif : carotte, navets, maïs, Maggi
  
  INSTRUCTIONS
  Mettre de l’huile dans la marmite. Quand c’est chaud mettre la viande et faire dorer, ajouter ensuite les oignons, poivron et ail coupés en dés. Laisser cuire 2 à 3mn puis rajouter environ 1,5 l d’eau et porter à ébullition.
  Réduire le feu et préparer le nokoss comme précédemment et cuire le riz à la vapeur.
  Rajouter par la suite le nokoss et le piment et faire cuire à feu doux pendant environ 30mns.
  Enlever le piment et rajouter le riz et laisser cuire à point.
  
  Ps : vous pouvez faire de la sauce d’oignons à part et rajouter les carottes et navets cuits coupés en dés et cuits à la vapeur et le maïs. Décorer également avec des œufs.
  Même principe pour le riz au poulet. Remplacer la viande par le poulet." 
      details.to_s
    end
  end
  
  
  module Yassa
    def self.desc
      details = "YASSA
  
  Du poisson rougets de préférence 
  1kg de riz
  1,5kg d’oignons 
  125ml d’huile 
  Poivron, céleri, ail, piment
  Sel, poivre, piment 
  Moutarde, citron, vinaigre 
  Facultatif : carotte, navets, œufs, Maggi
  
  INSTRUCTIONS
  Mixer du nokoss comme précédemment, trancher le poisson et mariner avec le nokoss rajouter le jus d’un demi citron et un peu de vinaigre puis réserver pour faire pénétrer la marinade.
  Pendant ce temps couper les oignons en fines lamelles, mariner avec le nokoss, moutarde, un peu de vinaigre et le jus d’un demi citron. Laisser reposer au moins 15mn. Laver le riz et faire cuire à la vapeur avec les légumes coupés en dés.
  Mettre de l’huile dans une marmite et rajouter les oignons, faire dorer un peu puis ajouter un peu d’eau et cuire à feu doux.
  Braiser les poissons ou mettre au four. Vous pouvez aussi les faire frire puis rajouter à la sauce. Rectifier l’assaisonnement puis éteindre le feu.
  Mettez environ 1l d’eau dans une autre marmite, saler et porter à ébullition. Rajouter le riz et enlever le surplus d’eau. Faire cuire à feu très doux. A mi-cuisson , rajouter les légumes coupés en dés.
  Dresser quand c’est prêt.
  
  PS : même principe pour le yassa au poulet et à la viande." 
      details.to_s
    end
  end
  
  
